 $pd_head .= "## visit www.sun.com/service\n";

// https://support.oracle.com/CSP/main/article?cmd=show&type=NOT&doctype=REFERENCE&id=1019527.1
 echo $pd_head;
 $a_p = Patchdiag::genFromPatches($ps->a_patches);
